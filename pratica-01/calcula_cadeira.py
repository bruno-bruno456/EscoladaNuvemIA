# Esse código tem como objetivo calcular o preço total de um produto

# Var com detalhes do produto
nome_produto = "Cadeira de Balanço"
preco_unitario = 20.60
quantidade = 5

# Var de cálculo do valor total
preco_total = quantidade * preco_unitario

# Print com o resultado final
print(f"Produto: {nome_produto}")
print(f"Preço unitário: R$ {preco_unitario:.2f}")
print(f"Quantidade: {quantidade}")
print(f"Preço total: R$ {preco_total:.2f}")